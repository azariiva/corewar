#include "../includes/libft.h"

int	ft_numlen(int a)
{
	int	len;

	len = 0;
	if (a < 0)
	{
		a *= -1;
		len++;
	}
	while (a)
	{
		a /= 10;
		len++;
	}
	return (len);
}
